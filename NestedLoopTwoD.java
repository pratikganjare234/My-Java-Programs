public class NestedLoopTwoD 
{
	public static void main(String args[])
	{
		int[][]pratik=new int[4][5];
		for(int row=0;row<4;row++)
		{
			for(int col=0;col<5;col++)
			{
				pratik[row][col]=5;
				System.out.println(pratik[row][col]+" ");
				
			}
			System.out.println();
		}
	}

}
